package com.example.queryapi.common.enums;

/**
 * Enum representing the execution status of a SQL query.
 */
public enum ExecutionStatus {
    /**
     * The SQL query was executed successfully.
     */
    SUCCESS,

    /**
     * The SQL query execution failed.
     */
    FAILURE,

    /**
     * An error occurred during the SQL query execution process.
     */
    ERROR
}

